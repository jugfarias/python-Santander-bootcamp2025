# Exemplos de estruturas de dados em Python

# Lista
lista = [1, 2, 3, 4, 5]

# Tupla
tupla = (10, 20, 30)

# Conjunto (set)
conjunto = {100, 200, 300}

# Dicionário
dicionario = {
    "nome": "Ana",
    "idade": 25,
    "cidade": "São Paulo"
}

# Exibindo os exemplos
print("Lista:", lista)
print("Tupla:", tupla)
print("Conjunto:", conjunto)
print("Dicionário:", dicionario)
